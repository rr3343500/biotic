package com.example.bioticclasses.List;

public class SliderList {
    String disc, url;

    public SliderList(String disc, String url) {
        this.disc = disc;
        this.url = url;
    }

    public String getDisc() {
        return disc;
    }

    public void setDisc(String disc) {
        this.disc = disc;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
